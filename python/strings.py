#Strings
#Incluir caracteres especiales en la cadena y que no generen conflicto \
cadena = 'Bienvenidos a \"Python\"'
print(cadena)
#Extrae un caracter de la cadena
saluda = 'Hola mundo'
print(saluda[3])
#Extraer una subcadena
print(saluda[5:10])
#Extraer subcadena desde la posicion index indicada hasta el final
print(saluda[2:])
#Multiplicar string
print(saluda*3)
#Concatenar Strings
print("Hola"+"concatenado")
#Concatenar Variables
variable = 'Hola'
variable += 'Mundooo'
variable += 'Python'
print(variable)